package basicsprograms;

public class ForLoop {
	
	public static void main(String[] args) {
		
//Printing 1 to 10
		String Name ="GroTechMind";
		for(int i=1;i<=10;i++) {
			System.out.println(i+"."+Name);
		}
	
//Printing 10 to 1
		
		String Name1 ="GroTech";
		for(int i=10;i>=1;i--) {
			System.out.println(i+"."+Name1);
		}
		
	}

}
